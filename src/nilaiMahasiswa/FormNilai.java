    public static String getGrade(double avg) {
        if (avg >= 90 && avg <= 100) {
            return "A";
        } else if (avg >= 85) {
            return "A-";
        } else if (avg >= 80) {
            return "B+";
        } else if (avg >= 75) {
            return "B";
        } else if (avg >= 70) {
            return "B-";
        } else if (avg >= 65) {
            return "C+";
        } else if (avg >= 60) {
            return "C";
        } else if (avg >= 55) {
            return "C-";
        } else if (avg >= 50) {
            return "D+";
        } else if (avg >= 45) {
            return "D";
        } else if (avg < 45 && avg >= 0) {
            return "E";
        } else {
            return "Invalid Value";
        }
    }


    private void buttonCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalcActionPerformed
        if (!textNIM.getText().trim().isEmpty() && !textNama.getText().trim().isEmpty() && !textTugas.getText().trim().isEmpty() && !textKuis.getText().trim().isEmpty() 
                && !textUTS.getText().trim().isEmpty() && !textUAS.getText().trim().isEmpty()) {            
            String nim = textNIM.getText();
            String nama = textNama.getText();
            int tugas = Integer.parseInt(textTugas.getText());
            int kuis = Integer.parseInt(textKuis.getText());
            int uts = Integer.parseInt(textUTS.getText());
            int uas = Integer.parseInt(textUAS.getText());    
        
            float avg = (float) (tugas + kuis + uts + uas) / 4;
            DecimalFormat decFormat = new DecimalFormat("#.00");
            String grade = getGrade(avg);
            String ket = (avg >= 70) ? "Lulus" : "Tidak Lulus";

            labelNIM.setText(nim);
            labelNama.setText(nama);
            labelAvg.setText(decFormat.format(avg));
            labelGrade.setText(grade);
            labelKeterangan.setText("Dinyatakan " + ket);
        } else {
            JOptionPane.showMessageDialog(null, "Lengkapi Input Terlebih Dahulu!");
        }
    }//GEN-LAST:event_buttonCalcActionPerformed


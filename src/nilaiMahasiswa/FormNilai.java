    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveActionPerformed
        try {
            st = conn.createStatement();

            // Validation
            if (!textNIM.getText().trim().isEmpty() && !textNama.getText().trim().isEmpty() && !textTugas.getText().trim().isEmpty() && !textKuis.getText().trim().isEmpty() 
                && !textUTS.getText().trim().isEmpty() && !textUAS.getText().trim().isEmpty()) {
                rs = st.executeQuery("SELECT * FROM nilai WHERE nim = " + textNIM.getText() + "");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "NIM : '" + textNIM.getText() + "' sudah dinilai!");
                    textNIM.setText("");
                } else {
                    st.executeUpdate("INSERT INTO nilai (nim, nama, tugas, kuis, uts, uas) VALUES('"
                            + textNIM.getText() + "','"
                            + textNama.getText() + "','"
                            + textTugas.getText() + "','"
                            + textKuis.getText() + "','"
                            + textUTS.getText() + "','"
                            + textUAS.getText() + "')");

                    JOptionPane.showMessageDialog(null, "Data tersimpan!");

                    ListNilai fl = new ListNilai();
                    fl.show();
                    dispose();
                }
            } else {
            JOptionPane.showMessageDialog(null, "Lengkapi Input Terlebih Dahulu!");
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_buttonSaveActionPerformed

